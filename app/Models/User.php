 * @method static \Illuminate\Database\Eloquent\Builder<static>|User whereSocialiteId($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|User whereUpdatedAt($value)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|User withoutPermission($permissions)
 * @method static \Illuminate\Database\Eloquent\Builder<static>|User withoutRole($roles, $guard = null)
 * @mixin \Eloquent
 */
class User extends Authenticatable
{
    /**
     * @use HasFactory<\Database\Factories\UserFactory>
     */
    use HasFactory, Notifiable, HasRoles, HasApiTokens;

    /**
     * The attributes that are mass assignable.
     *
     * @var list<string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'phone',
        'socialite_id',
        'email_verified_at',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var list<string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * @return mixed
     */
    public function UserTimeSlotAssignments()
    {
        return $this->hasMany(UserTimeSlotAssignment::class, 'user_id', 'id');
    }

    /**
     * @return mixed
     */
    public function dailyTimeSlots()
    {
        return $this->hasManyThrough(DailyTimeSlot::class, UserTimeSlotAssignment::class, 'user_id', 'id', 'id', 'daily_time_slot_id');
    }

    public function pets()
    {
        return $this->hasMany(Pet::class, 'user_id', 'id');
    }

    /**
     * Get the attributes that should be cast.
     *
     * @return array<string, string>
     */
    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password'          => 'hashed',
        ];
    }
}
